public class MoreNum {
    public static void main(String[] args) {
        int x = 5;
        int y = 6;
        int z = 50;
        System.out.println(x + y + z);

        int a = 7, b = 8, c = 42;
        System.out.println(a + b + c);

        int l, m, n;
        l = m = n = 42;
        System.out.println(l + m + n);
    }
}
